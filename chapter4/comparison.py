# Comparison Operators Example
x = 5
y = 3

print("Equal to:", x == y)                # False

print("Not equal to:", x != y)            # True

print("Greater than:", x > y)             # True


print("Less than:", x < y)                # False

print("Greater or equal to:", x >= y)     # True

print("Less or equal to:", x <= y)        # False


